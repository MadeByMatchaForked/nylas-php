<?php

namespace Inbox\Models;

class Thread
{
    use LazyLoader;

    /** @var string */
    protected $id;

    /**
     * @param string $id
     */
    public function __construct($id)
    {
        $this->id = $id;
    }

    /**
     * @return string
     */
    public function getId()
    {
        return $this->id;
    }
}